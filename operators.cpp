#include <iostream>

using namespace std;

int main()
{
    int a = 4, b = 5;
    cout << "Operators in C++:" << endl;
    cout << "Following are the operators:" << endl;
    cout << "The Value Of a + b is:" << a + b << endl;
    cout << "The Value Of a - b is:" << a - b << endl;
    cout << "The Value Of a * b is:" << a * b << endl;
    cout << "The Value Of a / b is:" << a / b << endl;
    cout << "The Value Of a % b is:" << a % b << endl;
    cout << "The Value Of a ++  is:" << a++ << endl;
    cout << "The Value Of a --  is:" << a-- << endl;
    cout << "The Value Of ++ a   is:" << ++a << endl;
    cout << "The Value Of -- a   is:" << --a << endl;

    return 0;
}